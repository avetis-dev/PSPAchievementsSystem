/* ---- 16-BIT DRAWING ---- */
static unsigned short make_565(unsigned int r, unsigned int g, unsigned int b) {
    return (unsigned short)(((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3));
}

static void fill_rect_16(unsigned short *fb, int bw, int x, int y, int w, int h, unsigned short c) {
    for (int ry = 0; ry < h; ry++) {
        int py = y + ry;
        if (py < 0 || py >= 272) continue;
        for (int rx = 0; rx < w; rx++) {
            int px = x + rx;
            if (px < 0 || px >= 480) continue;
            fb[py * bw + px] = c;
        }
    }
}

static void draw_text_16(unsigned short *fb, int bw, int x, int y, unsigned short c, const char *s) {
    int cx = x;
    while (*s) {
        if (*s >= 32 && *s <= 126) {
            int idx = *s - 32;
            for (int r = 0; r < 8; r++) {
                unsigned char row = font_data[idx][r];
                if (!row) continue;
                int py = y + r;
                if (py < 0 || py >= 272) continue;
                for (int col = 0; col < 8; col++) {
                    if (row & (0x80 >> col)) {
                        int px = cx + col;
                        if (px >= 0 && px < 480) fb[py * bw + px] = c;
                    }
                }
            }
        }
        cx += 8;
        s++;
    }
}

